import _ from 'lodash'
import Title from './title'
import Highlight from './highlight'
import React from 'react'

function AllRules({ rules }) {
  return (
    <div>
      <Title>All Rules</Title>
      <p>Below is a full list of all CSS selectors generated by Euphoria.</p>
      <Highlight lang="css">
        {_.map(rules, rule => rule.css).join('\n')}
      </Highlight>
    </div>
  )
}

export default AllRules
